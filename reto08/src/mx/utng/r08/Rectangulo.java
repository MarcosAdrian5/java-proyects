package mx.utng.r08;

public class Rectangulo {
    private double alto;
    private double ancho;

    public Rectangulo(){
        this.alto=0;
        this.ancho=0;
    }

    public Rectangulo(double lado){
        this.alto = lado;
        this.ancho = lado;
    }
    public Rectangulo(double alto, double ancho) {
        this.alto = alto;
        this.ancho = ancho;
    }


    
    
    }
